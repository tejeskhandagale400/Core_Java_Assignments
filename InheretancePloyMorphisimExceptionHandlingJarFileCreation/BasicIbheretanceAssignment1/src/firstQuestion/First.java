//Addition and subtraction



package firstQuestion;

public class First 
{
	private int numberOne, numberTwo;
	 

	First( int numberOne , int numberTwo)
	{
		this.numberOne = numberOne;
		this.numberTwo = numberTwo;
	}
	
	public double additionOfTwoNumbers()
	{
		
		return numberOne + numberTwo;
		
	}
	
	public double subtractionOfTwoNumbers()
	{
		
		return numberOne - numberTwo;
		
	}
	
	public double multificationOfTwoNumbers()
	{
		
		return numberOne * numberTwo;
		
	}
	
	public double divisionOfTwoNumbers()
	{
		
		return numberOne / numberTwo;
		
	}
	
}
